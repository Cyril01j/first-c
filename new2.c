#include<stdio.h>
int main()
    {
        // Creating variables having different data types
       int integer=2;
       float floating=29.69;
       char character='a';

        // Printing variables with the help of their respective format specifiers
        printf("%d\n", integer);
        printf("%f\n", floating);
        printf("%c\n", character);
        return 0;
    }
